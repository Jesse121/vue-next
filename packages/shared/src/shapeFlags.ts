export const enum ShapeFlags {
  ELEMENT = 1,
  FUNCTIONAL_COMPONENT = 1 << 1, // 2^1=2
  STATEFUL_COMPONENT = 1 << 2, // 2^2=4
  TEXT_CHILDREN = 1 << 3, // 2^3=8
  ARRAY_CHILDREN = 1 << 4, // 2^4=16
  SLOTS_CHILDREN = 1 << 5, // 2^5=32
  TELEPORT = 1 << 6, // 2^6=64
  SUSPENSE = 1 << 7, // 2^7=128
  COMPONENT_SHOULD_KEEP_ALIVE = 1 << 8, // 2^8=256
  COMPONENT_KEPT_ALIVE = 1 << 9, // 2^9=512
  COMPONENT = ShapeFlags.STATEFUL_COMPONENT | ShapeFlags.FUNCTIONAL_COMPONENT
}
